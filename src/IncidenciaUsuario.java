    private Integer obtenerIdDispositivo(String nombre) throws Exception {
        if (nombre == null || nombre.isEmpty()) {
            return null;
        }

        Connection conn = ConexionDB.conectar();

        String sql = "SELECT id FROM DISPOSITIVO WHERE nombre = ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, nombre);

        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            Integer id = rs.getInt("id");
            rs.close();
            ps.close();
            conn.close();
            return id;
        }

        rs.close();
        ps.close();
        conn.close();
        return null;
    }

    private int obtenerGestorAleatorio() throws Exception {
        Connection conn = ConexionDB.conectar();

        String sql = "SELECT id FROM USUARIO WHERE id_perfil = 3 ORDER BY RANDOM() LIMIT 1";
        PreparedStatement ps = conn.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            int idGestor = rs.getInt("id");
            rs.close();
            ps.close();
            conn.close();
            return idGestor;
        }

        rs.close();
        ps.close();
        conn.close();
        throw new Exception("No hay gestores disponibles");
    }

    private void jButtonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearActionPerformed
        // TODO add your handling code here:
        String tipoIncidencia = (String) jComboBoxTipo.getSelectedItem();
        String espacio = (String) jComboBoxEspacio.getSelectedItem();
        String dispositivo = (String) jComboBoxDispositivo.getSelectedItem();
        String prioridad = (String) jComboBoxPrioridad.getSelectedItem();
        String descripcion = jTextAreaDescripcion.getText();

        if (descripcion.isBlank()) {
            JOptionPane.showMessageDialog(this, "La descripción no puede estar vacía");
            return;
        }

        try {
            int idTipo = obtenerIdTipoIncidencia(tipoIncidencia);
            int idEspacio = obtenerIdEspacio(espacio);
            Integer idDispositivo = obtenerIdDispositivo(dispositivo);
            int idGestor = obtenerGestorAleatorio();

            Connection conn = ConexionDB.conectar();

            String sql = """
            INSERT INTO INCIDENCIA (
                id_tipo,
                id_espacio,
                id_dispositivo,
                id_usuario_reporta,
                id_gestor_asigna,
                prioridad,
                estado,
                descripcion_incidencia
            ) VALUES (?, ?, ?, ?, ?, ?, 'Abierta', ?)
        """;

            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, idTipo);
            ps.setInt(2, idEspacio);
            ps.setObject(3, idDispositivo);
            ps.setInt(4, this.idUsuario);
            ps.setInt(5, idGestor);
            ps.setString(6, prioridad);
            ps.setString(7, descripcion);

            ps.executeUpdate();

            ps.close();
            conn.close();

            JOptionPane.showMessageDialog(this, "Incidencia creada correctamente");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al crear incidencia: " + e.getMessage());
        }


    }//GEN-LAST:event_jButtonCrearActionPerformed

