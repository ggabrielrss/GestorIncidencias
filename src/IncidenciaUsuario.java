    private int obtenerIdTipoIncidencia(String descripcion) throws Exception {
        Connection conn = ConexionDB.conectar();

        String sql = "SELECT id FROM TIPO_INCIDENCIA WHERE descripcion = ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, descripcion);

        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            int id = rs.getInt("id");
            rs.close();
            ps.close();
            conn.close();
            return id;
        }

        rs.close();
        ps.close();
        conn.close();
        throw new Exception("Tipo de incidencia no encontrado");
    }

