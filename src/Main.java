    private void jButtonIniciarSesionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonIniciarSesionMouseClicked
        // TODO add your handling code here:
        String usuario = jTextNombreUsuario.getText();
        String contraseña = new String(jPasswordContraseñaUsuario.getPassword());
        String tipoSeleccionado = (String) jComboBoxTipo.getSelectedItem();

        try (Connection conn = ConexionDB.conectar()) {

            String sql = "SELECT u.id, u.nombre, u.apellidos, p.nombre AS perfil "
                    + "FROM USUARIO u "
                    + "JOIN PERFIL p ON u.id_perfil = p.id "
                    + "WHERE u.nombre = ? AND u.contrasena = ?";

            try (PreparedStatement ps = conn.prepareStatement(sql)) {
                ps.setString(1, usuario);
                ps.setString(2, contraseña);
                ResultSet rs = ps.executeQuery();

                if (rs.next()) {
                    int idUsuario = rs.getInt("id");
                    String nombreCompleto = rs.getString("nombre") + " " + rs.getString("apellidos");
                    String perfilReal = rs.getString("perfil"); // Perfil real desde la DB

                    if (!tipoSeleccionado.equals(perfilReal)) {
                        JOptionPane.showMessageDialog(this,
                                "Tipo de usuario incorrecto",
                                "Error de acceso",
                                JOptionPane.ERROR_MESSAGE
                        );
                        jPasswordContraseñaUsuario.setText("");
                        return;
                    }

                    switch (perfilReal) {
                        case "Usuario":
                            VistaUsuario vUser = new VistaUsuario(idUsuario, nombreCompleto);
                            vUser.setVisible(true);
                            break;
                        case "Técnico":
                            VistaTecnico vTec = new VistaTecnico(idUsuario, nombreCompleto);
                            vTec.setVisible(true);
                            break;
                        case "Gestor":
                            VistaGestor vg = new VistaGestor(idUsuario, nombreCompleto);
                            vg.setVisible(true);
                            break;
                        case "Admin":
                            VistaAdmin vAdminGestor = new VistaAdmin(idUsuario, nombreCompleto);
                            vAdminGestor.setVisible(true);
                            break;
                    }

                    this.dispose();

                } else {
                    JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos", "Error de acceso", JOptionPane.ERROR_MESSAGE);
                    jPasswordContraseñaUsuario.setText("");
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al conectar con la base de datos: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButtonIniciarSesionMouseClicked

