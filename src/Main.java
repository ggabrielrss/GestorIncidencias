    private boolean validarUsuario(String usuario, String contraseña, String tipo) {
        Connection conn = ConexionDB.conectar();
        if (conn == null) {
            return false;
        }

        String sql = "SELECT U.id, P.nombre "
                + "FROM USUARIO U "
                + "JOIN PERFIL P ON U.id_perfil = P.id "
                + "WHERE U.nombre = ? AND U.contrasena = ? AND P.nombre = ?";

        try (PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setString(1, usuario);
            ps.setString(2, contraseña);
            ps.setString(3, tipo);

            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                return true;
            } else {
                return false;
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al consultar usuario:\n" + e.getMessage());
            return false;
        } finally {
            try {
                conn.close();
            } catch (SQLException ignored) {
            }
        }
    }

