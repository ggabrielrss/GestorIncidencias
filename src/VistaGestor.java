    private void jButtorDarAltaIncidenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtorDarAltaIncidenciaActionPerformed
        String idIncidenciaStr = jTextFieldIdIncidencia.getText().trim();
        String nombreTecnico = jTextFieldTecnico.getText().trim();

        if (idIncidenciaStr.isEmpty() || nombreTecnico.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, rellena ambos campos.");
            return;
        }

        try (Connection conn = ConexionDB.conectar()) {
            String sqlCheck = "SELECT estado FROM INCIDENCIA WHERE id = ?";
            PreparedStatement psCheck = conn.prepareStatement(sqlCheck);
            psCheck.setInt(1, Integer.parseInt(idIncidenciaStr));
            ResultSet rs = psCheck.executeQuery();

            if (!rs.next()) {
                JOptionPane.showMessageDialog(this, "La incidencia con ID " + idIncidenciaStr + " no existe.");
                return;
            }

            if (!"En espera".equals(rs.getString("estado"))) {
                JOptionPane.showMessageDialog(this, "Solo se pueden asignar incidencias que estén 'En espera'.");
                return;
            }

            String sqlTecnico = "SELECT id FROM USUARIO WHERE nombre = ? AND id_perfil = 2";
            PreparedStatement psTec = conn.prepareStatement(sqlTecnico);
            psTec.setString(1, nombreTecnico);
            ResultSet rsTec = psTec.executeQuery();

            if (!rsTec.next()) {
                JOptionPane.showMessageDialog(this, "No se encontró ningún técnico con el nombre: " + nombreTecnico);
                return;
            }
            int idTecnico = rsTec.getInt("id");

            String sqlUpdate = "UPDATE INCIDENCIA SET id_tecnico_asignado = ?, estado = 'Asignada' WHERE id = ?";
            PreparedStatement psUpdate = conn.prepareStatement(sqlUpdate);
            psUpdate.setInt(1, idTecnico);
            psUpdate.setInt(2, Integer.parseInt(idIncidenciaStr));

            int filas = psUpdate.executeUpdate();
            if (filas > 0) {
                JOptionPane.showMessageDialog(this, "Incidencia asignada correctamente a " + nombreTecnico);
                this.dispose(); // Cierra esta ventana tras el éxito
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID de la incidencia debe ser un número.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButtorDarAltaIncidenciaActionPerformed

