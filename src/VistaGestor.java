    private void jButtorListarEsperaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtorListarEsperaActionPerformed

        String[] columnas = {"ID", "Fecha Entrada", "Descripción"};
    javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(columnas, 0);

    String sql = "SELECT id, fecha_entrada, descripcion_incidencia FROM INCIDENCIA WHERE estado = 'En espera'";

    try (Connection conn = ConexionDB.conectar()) {
        if (conn == null) return;

        try (PreparedStatement ps = conn.prepareStatement(sql);
             ResultSet rs = ps.executeQuery()) {

            boolean hayDatos = false;
            while (rs.next()) {
                hayDatos = true;
                Object[] fila = {
                    rs.getInt("id"),
                    rs.getString("fecha_entrada"),
                    rs.getString("descripcion_incidencia")
                };
                modelo.addRow(fila);
            }

            if (!hayDatos) {
                JOptionPane.showMessageDialog(this, "No hay ninguna incidencia en estado 'En espera' actualmente.");
            } else {
                javax.swing.JTable tabla = new javax.swing.JTable(modelo);
                javax.swing.JScrollPane scrollPane = new javax.swing.JScrollPane(tabla);
                scrollPane.setPreferredSize(new java.awt.Dimension(600, 300));

                JOptionPane.showMessageDialog(this, scrollPane, "Incidencias En Espera", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al listar incidencias en espera: " + e.getMessage());
        e.printStackTrace();
    }


    }//GEN-LAST:event_jButtorListarEsperaActionPerformed

