    private void jButtonEsperaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEsperaActionPerformed
        // TODO add your handling code here:
        String idIncidenciaStr = jTextFieldIdIncidencia.getText().trim();

    if (idIncidenciaStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, introduce el ID de la incidencia en el campo 'Id incidencia'.");
        return;
    }

    String sql = "UPDATE INCIDENCIA SET estado = 'En espera' WHERE id = ?";

    try (Connection conn = ConexionDB.conectar()) {
        if (conn == null) return;

        try (PreparedStatement ps = conn.prepareStatement(sql)) {
            int idIncidencia = Integer.parseInt(idIncidenciaStr);
            ps.setInt(1, idIncidencia);

            int filasAfectadas = ps.executeUpdate();

            if (filasAfectadas > 0) {
                JOptionPane.showMessageDialog(this, "La incidencia con ID " + idIncidencia + " ha sido puesta 'En espera' correctamente.");
                jTextFieldIdIncidencia.setText(""); // Limpiamos el campo tras el éxito
            } else {
                JOptionPane.showMessageDialog(this, "No se encontró ninguna incidencia con el ID: " + idIncidencia);
            }
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Error: El ID de la incidencia debe ser un número válido.");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al conectar con la base de datos: " + e.getMessage());
        e.printStackTrace();
    }
        
        
    }//GEN-LAST:event_jButtonEsperaActionPerformed

    private void jButtorListarTecnicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtorListarTecnicoActionPerformed
        // TODO add your handling code here:
        String nombreTecnico = jTextFieldTecnico.getText().trim();

    if (nombreTecnico.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, introduce el nombre del técnico.");
        return;
    }

    String[] columnas = {"ID", "Técnico", "Tipo", "Prioridad", "Estado", "Descripción"};
    javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(columnas, 0);

    String sql = "SELECT i.id, u.nombre, t.descripcion, i.prioridad, i.estado, i.descripcion_incidencia "
               + "FROM INCIDENCIA i "
               + "JOIN TIPO_INCIDENCIA t ON i.id_tipo = t.id "
               + "JOIN USUARIO u ON i.id_tecnico_asignado = u.id "
               + "WHERE u.nombre = ?";

    try (Connection conn = ConexionDB.conectar()) {
        if (conn == null) {
            return;
        }

        try (PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setString(1, nombreTecnico);

            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                Object[] fila = {
                    rs.getInt("id"),
                    rs.getString("nombre"), 
                    rs.getString("descripcion"),
                    rs.getString("prioridad"),
                    rs.getString("estado"),
                    rs.getString("descripcion_incidencia")
                };
                modelo.addRow(fila);
            }

            if (modelo.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "No se encontraron incidencias asignadas exactamente al técnico: " + nombreTecnico);
            } else {
                javax.swing.JTable tabla = new javax.swing.JTable(modelo);
                javax.swing.JScrollPane scrollPane = new javax.swing.JScrollPane(tabla);
                scrollPane.setPreferredSize(new java.awt.Dimension(850, 400));

                JOptionPane.showMessageDialog(this, scrollPane, "Incidencias asignadas a: " + nombreTecnico, JOptionPane.PLAIN_MESSAGE);
            }
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al filtrar por técnico: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButtorListarTecnicoActionPerformed

