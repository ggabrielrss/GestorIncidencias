    private void jButtonAgregar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregar2ActionPerformed
        String fechaIni = jTextFieldFechaInicio1.getText().trim();
        String fechaFin = jTextFieldFechaInicio.getText().trim();

        if (fechaIni.isEmpty() || fechaFin.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, introduce ambas fechas (formato YYYY-MM-DD).");
            return;
        }

        String[] columnas = {"ID", "Estado", "Prioridad", "Fecha Entrada", "Descripción"};
        javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(columnas, 0);

        String sql = "SELECT id, estado, prioridad, fecha_entrada, descripcion_incidencia "
                + "FROM INCIDENCIA "
                + "WHERE id_tecnico_asignado = ? "
                + "AND fecha_entrada BETWEEN ? AND ? ORDER BY fecha_entrada ASC";

        try (Connection conn = ConexionDB.conectar()) {
            if (conn == null) {
                return;
            }

            try (PreparedStatement ps = conn.prepareStatement(sql)) {
                ps.setInt(1, this.idUsuario);
                ps.setString(2, fechaIni + " 00:00:00");
                ps.setString(3, fechaFin + " 23:59:59");

                ResultSet rs = ps.executeQuery();

                while (rs.next()) {
                    Object[] fila = {
                        rs.getInt("id"),
                        rs.getString("estado"),
                        rs.getString("prioridad"),
                        rs.getString("fecha_entrada"),
                        rs.getString("descripcion_incidencia")
                    };
                    modelo.addRow(fila);
                }

                if (modelo.getRowCount() == 0) {
                    JOptionPane.showMessageDialog(this, "No hay incidencias asignadas entre " + fechaIni + " y " + fechaFin);
                } else {
                    javax.swing.JTable tabla = new javax.swing.JTable(modelo);
                    javax.swing.JScrollPane scrollPane = new javax.swing.JScrollPane(tabla);
                    scrollPane.setPreferredSize(new java.awt.Dimension(800, 400));
                    JOptionPane.showMessageDialog(this, scrollPane, "Incidencias del periodo", JOptionPane.PLAIN_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al consultar por fechas: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonAgregar2ActionPerformed

