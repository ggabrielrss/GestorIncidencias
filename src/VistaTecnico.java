    private void jButtonResolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonResolverActionPerformed

        String idTexto = jTextFieldIdResolver.getText().trim();

        if (idTexto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes introducir el ID de la incidencia.");
            return;
        }

        int idIncidencia;
        try {
            idIncidencia = Integer.parseInt(idTexto);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Introduce un ID numérico válido.");
            return;
        }

        try (Connection conn = ConexionDB.conectar()) {
            if (conn == null) {
                return;
            }

            String sql = "SELECT estado, id_tecnico_asignado FROM INCIDENCIA WHERE id = ?";
            try (PreparedStatement ps = conn.prepareStatement(sql)) {
                ps.setInt(1, idIncidencia);
                ResultSet rs = ps.executeQuery();

                if (!rs.next()) {
                    JOptionPane.showMessageDialog(this, "No se encontró ninguna incidencia con ese ID.");
                    return;
                }

                String estado = rs.getString("estado");
                int idTecnicoAsignado = rs.getInt("id_tecnico_asignado");

                if (idTecnicoAsignado != this.idUsuario) {
                    JOptionPane.showMessageDialog(this, "Esta incidencia no está asignada a tu ID.");
                    return;
                }

                if ("En espera".equalsIgnoreCase(estado)) {
                    JOptionPane.showMessageDialog(this, "No puedes resolver una incidencia en estado 'En espera'.");
                    return;
                }

                if ("Resuelta".equalsIgnoreCase(estado)) {
                    JOptionPane.showMessageDialog(this, "No puedes resolver una incidencia en estado 'Resuelta'.");
                    return;
                }

                IncidenciaTecnico ventana = new IncidenciaTecnico(idIncidencia);
                ventana.setVisible(true);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al validar la incidencia: " + e.getMessage());
            e.printStackTrace();
        }


    }//GEN-LAST:event_jButtonResolverActionPerformed

    private void jButtonVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVerActionPerformed
        String[] columnas = {"ID", "Tipo", "Prioridad", "Estado", "Descripción", "Fecha Entrada"};
        javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(columnas, 0);

        String sql = "SELECT i.id, t.descripcion, i.prioridad, i.estado, i.descripcion_incidencia, i.fecha_entrada "
                + "FROM INCIDENCIA i "
                + "JOIN TIPO_INCIDENCIA t ON i.id_tipo = t.id "
                + "WHERE i.id_tecnico_asignado = ?";

        try (Connection conn = ConexionDB.conectar()) {
            if (conn == null) {
                return;
            }

            try (PreparedStatement ps = conn.prepareStatement(sql)) {
                ps.setInt(1, this.idUsuario);
                ResultSet rs = ps.executeQuery();

                while (rs.next()) {
                    Object[] fila = {
                        rs.getInt("id"),
                        rs.getString("descripcion"),
                        rs.getString("prioridad"),
                        rs.getString("estado"),
                        rs.getString("descripcion_incidencia"),
                        rs.getString("fecha_entrada")
                    };
                    modelo.addRow(fila);
                }

                javax.swing.JTable tabla = new javax.swing.JTable(modelo);
                javax.swing.JScrollPane scrollPane = new javax.swing.JScrollPane(tabla);
                scrollPane.setPreferredSize(new java.awt.Dimension(800, 400));

                if (modelo.getRowCount() == 0) {
                    JOptionPane.showMessageDialog(this, "No tienes incidencias asignadas actualmente.");
                } else {
                    JOptionPane.showMessageDialog(this, scrollPane, "Mis Incidencias Asignadas", JOptionPane.PLAIN_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar tus incidencias: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButtonVerActionPerformed

    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed
        // TODO add your handling code here:

        String descripcion = jTextFieldNuevoTipo.getText().trim();

        if (descripcion.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduce una descripción para el tipo de incidencia.");
            return;
        }

        try (Connection conn = ConexionDB.conectar()) {
            if (conn == null) {
                return;
            }

            String checkSql = "SELECT id FROM TIPO_INCIDENCIA WHERE LOWER(descripcion) = LOWER(?)";
            try (PreparedStatement checkPs = conn.prepareStatement(checkSql)) {
                checkPs.setString(1, descripcion);
                ResultSet rs = checkPs.executeQuery();

                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Ese tipo de incidencia ya existe.");
                    return;
                }
            }

            String insertSql = "INSERT INTO TIPO_INCIDENCIA (descripcion) VALUES (?)";
            try (PreparedStatement insertPs = conn.prepareStatement(insertSql)) {
                insertPs.setString(1, descripcion);
                insertPs.executeUpdate();
            }

            JOptionPane.showMessageDialog(this, "Tipo de incidencia agregado correctamente.");
            jTextFieldNuevoTipo.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al agregar tipo de incidencia: " + e.getMessage());
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButtonAgregarActionPerformed

