    private void jButtonConsultarIncidenciasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarIncidenciasActionPerformed
        // TODO add your handling code here:
        String idTexto = jTextFieldIdConsulta.getText().trim();

        if (idTexto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduce un ID de incidencia", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int idIncidencia;
        try {
            idIncidencia = Integer.parseInt(idTexto);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID debe ser un número", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try (Connection conn = ConexionDB.conectar()) {
            if (conn == null) {
                return;
            }

            String sql = """
        SELECT id,
               descripcion_incidencia,
               estado,
               fecha_entrada,
               descripcion_solucion,
               fecha_cierre
        FROM INCIDENCIA
        WHERE id_usuario_reporta = ? AND id = ?
        """;

            try (PreparedStatement ps = conn.prepareStatement(sql)) {
                ps.setInt(1, idUsuario);
                ps.setInt(2, idIncidencia);

                ResultSet rs = ps.executeQuery();
                StringBuilder sb = new StringBuilder();

                if (rs.next()) {
                    sb.append("ID: ").append(rs.getInt("id"))
                            .append("\nEstado: ").append(rs.getString("estado"))
                            .append("\nFecha entrada: ").append(rs.getString("fecha_entrada"))
                            .append("\nDescripción: ").append(rs.getString("descripcion_incidencia"));

                    String solucion = rs.getString("descripcion_solucion");
                    String fechaCierre = rs.getString("fecha_cierre");

                    if (solucion != null && !solucion.isEmpty()) {
                        sb.append("\nSolución: ").append(solucion);
                        if (fechaCierre != null) {
                            sb.append("\nFecha cierre: ").append(fechaCierre);
                        }
                    }
                }

                if (sb.length() == 0) {
                    JOptionPane.showMessageDialog(
                            this,
                            "No se encontró ninguna incidencia con ese ID para tu usuario.",
                            "Incidencias",
                            JOptionPane.INFORMATION_MESSAGE
                    );
                } else {
                    JOptionPane.showMessageDialog(
                            this,
                            sb.toString(),
                            "Incidencia #" + idIncidencia,
                            JOptionPane.INFORMATION_MESSAGE
                    );
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Error al consultar la incidencia: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
            e.printStackTrace();
        }


    }//GEN-LAST:event_jButtonConsultarIncidenciasActionPerformed

