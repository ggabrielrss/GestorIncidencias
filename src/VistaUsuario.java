    private void jButtonListarIncidenciasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarIncidenciasActionPerformed
        // TODO add your handling code here:
        String[] columnas = {"ID", "Estado", "Fecha Entrada", "Descripción", "Solución", "Fecha Cierre"};
    javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(columnas, 0);

    try (Connection conn = ConexionDB.conectar()) {
        if (conn == null) return;

        String sql = "SELECT id, estado, fecha_entrada, descripcion_incidencia, descripcion_solucion, fecha_cierre "
                   + "FROM INCIDENCIA WHERE id_usuario_reporta = ?"; 

        try (PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setInt(1, idUsuario); 

            ResultSet rs = ps.executeQuery();
            boolean hayDatos = false;

            while (rs.next()) {
                hayDatos = true;
                Object[] fila = {
                    rs.getInt("id"),
                    rs.getString("estado"),
                    rs.getString("fecha_entrada"),
                    rs.getString("descripcion_incidencia"),
                    rs.getString("descripcion_solucion") == null ? "" : rs.getString("descripcion_solucion"),
                    rs.getString("fecha_cierre") == null ? "" : rs.getString("fecha_cierre")
                };
                modelo.addRow(fila);
            }

            if (!hayDatos) {
                JOptionPane.showMessageDialog(this, "No tienes incidencias registradas.", "Incidencias", JOptionPane.INFORMATION_MESSAGE);
            } else {
                javax.swing.JTable tabla = new javax.swing.JTable(modelo);
                tabla.setEnabled(false); // Para que sea solo de lectura
                
                javax.swing.JScrollPane scrollPane = new javax.swing.JScrollPane(tabla);
                scrollPane.setPreferredSize(new java.awt.Dimension(800, 400));

                JOptionPane.showMessageDialog(this, scrollPane, "Mis Incidencias", JOptionPane.INFORMATION_MESSAGE);
            }
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al consultar incidencias: " + e.getMessage());
        e.printStackTrace();
    }

    }//GEN-LAST:event_jButtonListarIncidenciasActionPerformed

    private void jButtonSolicitarReaperturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSolicitarReaperturaActionPerformed
        // TODO add your handling code here:
        String idTexto = jTextFieldIdReapertura.getText().trim();

        if (idTexto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduce un ID de incidencia", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int idIncidencia;
        try {
            idIncidencia = Integer.parseInt(idTexto);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID debe ser un número", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Connection conn = ConexionDB.conectar();
        if (conn == null) {
            return;
        }

        try {
            String sqlSelect = "SELECT estado FROM INCIDENCIA WHERE id = ? AND id_usuario_reporta = ?";
            PreparedStatement psSelect = conn.prepareStatement(sqlSelect);
            psSelect.setInt(1, idIncidencia);
            psSelect.setInt(2, idUsuario);

            ResultSet rs = psSelect.executeQuery();

            if (!rs.next()) {
                JOptionPane.showMessageDialog(this, "No se encontró ninguna incidencia con ese ID para tu usuario.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                rs.close();
                psSelect.close();
                conn.close();
                return;
            }

            String estadoActual = rs.getString("estado");
            rs.close();
            psSelect.close();

            if (!(estadoActual.equals("Resuelta"))) {
                JOptionPane.showMessageDialog(this, "No se puede reabrir una incidencia que no esté resuelta.", "Aviso", JOptionPane.WARNING_MESSAGE);
                conn.close();
                return;
            }
            // Cambiar estado a "En espera" y registrar fecha de reapertura
            String sqlUpdate = "UPDATE INCIDENCIA SET estado = 'En espera', fecha_reapertura = CURRENT_TIMESTAMP WHERE id = ?";
            PreparedStatement psUpdate = conn.prepareStatement(sqlUpdate);
            psUpdate.setInt(1, idIncidencia);

            int filas = psUpdate.executeUpdate();
            if (filas > 0) {
                JOptionPane.showMessageDialog(this, "Incidencia #" + idIncidencia + " solicitada para reapertura correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo solicitar la reapertura.", "Error", JOptionPane.ERROR_MESSAGE);
            }

            psUpdate.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al solicitar la reapertura: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButtonSolicitarReaperturaActionPerformed

